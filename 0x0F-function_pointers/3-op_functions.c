#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: first number
 * @b: second number
 *
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: multiple
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: division of intergers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: modulo of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
